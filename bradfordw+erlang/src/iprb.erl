-module(iprb).

%% API
-export([iprb/3]).
-include_lib("eunit/include/eunit.hrl").

%%%===================================================================
%%% API
%%%===================================================================
iprb(K, M, N) ->
    Total = K + M + N,
    SumProb = (N * (N - 1) + N * M + M * (M - 1) / 4.0),
    (1 - 1 / Total / (Total - 1) * SumProb).
%% @doc
%% @spec
%% @end
%%--------------------------------------------------------------------

%%%===================================================================
%%% Internal functions
%%%===================================================================
iprb_test() ->
    ?assertEqual(0.7833333333333333, iprb(2,2,2)).
